# المشروع الأول: نظام معالجة درجات الطلاب وتعديلها (Broadcasting & Statistics)
# الفكرة: حساب مخرجات إحصائية لدرجات الطلاب، وإضافة درجات مساعدة لجميع الطلاب دفعة واحدة.
# ----------------------------------------------------------------------------------------------------------
import numpy as np
print("\n--- المشروع 1: نظام درجات الطلاب ---")

# درجات 5 طلاب في 3 امتحانات
grades = np.array([[75, 80, 85],
                   [55, 60, 58],
                   [90, 92, 95],
                   [45, 50, 48],
                   [68, 74, 70]])

# 1. حساب المتوسط الحسابي لكل طالب (عبر المحور 1 - الأعمدة)
students_average = np.mean(grades, axis=1)
print("المعدل الحالي لكل طالب:", students_average)

# 2. إضافة 5 درجات مساعدة لجميع الطلاب كقرار عام بفضل الـ Broadcasting
boosted_grades = grades + 5
print("الدرجات بعد إضافة المساعدة:\n", boosted_grades)

# 3. معرفة أعلى درجة سُجلت في كل امتحان (عبر المحور 0 - الصفوف)
highest_per_exam = np.max(grades, axis=0)
print("أعلى درجة في الامتحانات الثلاثة:", highest_per_exam)


# ----------------------------------------------------------------------------------------------------------
# المشروع الثاني: فلترة وتنظيف بيانات المبيعات (Indexing, Slicing & Searching)
# الفكرة: عزل أرقام المبيعات الضعيفة وتصفية القيم الشاذة أو الفريدة.
# ----------------------------------------------------------------------------------------------------------
print("\n--- المشروع 2: تحليل وتصفية المبيعات ---")

# مصفوفة مبيعات يومية لمتجر على مدار أسبوعين (تحتوي على قيم متكررة وصفير)
sales = np.array([120, 0, 150, 300, 0, 450, 120, 0, 500, 300, 150, 600, 750, 0])

# 1. إيجاد الأيام التي كانت فيها المبيعات صفراً (أيام الإجازات أو الخلل) باستخدام where
zero_sales_days = np.where(sales == 0)
print("مواقع الأيام الخالية من المبيعات (Indexes):", zero_sales_days[0])

# 2. فلترة البيانات واستخراج المبيعات الفعلية فقط (أكبر من صفر) عبر الشروط المباشرة
active_sales = sales[sales > 0]
print("المبيعات الفعالة فقط:", active_sales)

# 3. استخراج قيم الأرباح الفريدة التي حققها المتجر بدون تكرار
unique_profits = np.sort(np.unique(active_sales))
print("قيم الأرباح الفريدة مرتبة تصاعدياً:", unique_profits)


# ----------------------------------------------------------------------------------------------------------
# المشروع الثالث: تشفير وحماية الصور الرقمية (Matrix Operations & Transpose)
# الفكرة: محاكاة لتشفير مصفوفة صورة (أبيض وأسود) عبر تدويرها وعكس مصفوفة الألوان الرقمية.
# ----------------------------------------------------------------------------------------------------------
print("\n--- المشروع 3: تشفير وقراءة مصفوفة صور رقمية ---")

# مصفوفة تمثل بكسلات صورة مصغرة (أبعاد 3x3) قيمها بين 0 و 255
original_image = np.array([[10,  150, 200],
                           [85,  255, 45],
                           [190, 30,  120]])

# 1. تشفير الصورة الأولى: تدوير الماتريكس وجعل الصفوف أعمدة باستخدام الـ Transpose
encrypted_step1 = original_image.T

# 2. تشفير خطوة ثانية: عكس الألوان (طرح المصفوفة من 255 وهو الحد الأقصى للألوان الحسابية للـ Pixel)
encrypted_final = 255 - encrypted_step1
print("مصفوفة الصورة بعد التشفير الرقمي التام:\n", encrypted_final)

# 3. فك التشفير لإرجاع الصورة الأصلية (عكس الألوان ثم التدوير مجدداً)
decrypted_image = (255 - encrypted_final).T
print("إعادة الصورة لأصلها لضمان صحة العملية:\n", decrypted_image)

